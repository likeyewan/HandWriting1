package com.example.handwriting.bean;

public class Achei {
    private String name;
    private int photo;
    private int needs;
    private int type;
    private int state;
    private int photoN;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPhoto() {
        return photo;
    }

    public void setPhoto(int photo) {
        this.photo = photo;
    }

    public int getNeeds() {
        return needs;
    }

    public void setNeeds(int needs) {
        this.needs = needs;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public int getState() {
        return state;
    }

    public void setState(int state) {
        this.state = state;
    }

    public int getPhotoN() {
        return photoN;
    }

    public void setPhotoN(int photoN) {
        this.photoN = photoN;
    }
}
